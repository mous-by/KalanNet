    <h4 class="fw-bold text-uppercase mt-5 mb-3 border-bottom pb-2">Étape 1 — Accès et Tableau de Bord</h4>
    <h5 class="fw-bold mt-4">Connexion et Contexte École</h5>
    <ul>
        <li>Connectez-vous par email ou numéro de téléphone.</li>
        <li>Si vous gérez plusieurs écoles, vous devez sélectionner votre école de travail après la connexion.</li>
        <li>En cas d'abonnement expiré, le système restreindra l'accès jusqu'au renouvellement.</li>
    </ul>
    <h5 class="fw-bold mt-4">Tableau de bord</h5>
    <p>Il donne une vue synthétique de l'établissement : effectifs, statistiques, indicateurs financiers, et paiements récents selon vos droits.</p>

    <h4 class="fw-bold text-uppercase mt-5 mb-3 border-bottom pb-2">Étape 2 — Configuration</h4>
    <p>Avant de démarrer l'année scolaire, le menu <strong>Configuration</strong> doit être rempli :</p>
    <ul>
        <li><strong>Années scolaires :</strong> Définissez les dates de début et de fin.</li>
        <li><strong>Académies & CAP :</strong> Utiles pour les rapports et PDF officiels.</li>
        <li><strong>Écoles :</strong> Configuration de votre établissement, logo (formats jpg, png, webp) et activation des notifications par email ou SMS.</li>
        <li><strong>Utilisateurs et Permissions :</strong> Création de comptes avec droits stricts (Ex: aperçu, création, paiement).</li>
        <li><strong>Classes officielles et Types de notes :</strong> Paramétrez l'architecture pédagogique.</li>
        <li><strong>Statuts de contrôle :</strong> Configurez les alertes et les pénalités de conduite (ex: retard, absence).</li>
    </ul>

    <h4 class="fw-bold text-uppercase mt-5 mb-3 border-bottom pb-2">Étape 3 — Pédagogie (Élèves & Inscriptions)</h4>
    <h5 class="fw-bold mt-4">Inscriptions</h5>
    <ul>
        <li><strong>Individuelle :</strong> Saisie classique. Le matricule peut être généré automatiquement.</li>
        <li><strong>Par groupe (Import Excel) :</strong> Pour créer plusieurs élèves à la fois via un modèle téléchargeable.</li>
        <li><strong>Réinscription intelligente :</strong> Par classe, le système propose le passage ou le redoublement en fonction de la moyenne annuelle. Les élèves peuvent aussi être transférés, ajournés, abandonnés ou exclus.</li>
    </ul>
    <h5 class="fw-bold mt-4">Gestion des Élèves</h5>
    <ul>
        <li><strong>Liste des Élèves :</strong> Filtrage par classe, impression PDF, export Excel, et enregistrement de transferts.</li>
        <li><strong>Dossiers Élèves :</strong> Une vue complète à 360° : historique, pédagogie, et paiements avec accès aux reçus.</li>
        <li><strong>Cartes Scolaires :</strong> Personnalisation et impression (institutionnel, moderne, etc.) avec ajout du QR code (qui contient les infos complètes de l'élève) et de la signature électronique de l'administration.</li>
    </ul>

    <h4 class="fw-bold text-uppercase mt-5 mb-3 border-bottom pb-2">Étape 4 — Contrôles et Bulletins</h4>
    <h5 class="fw-bold mt-4">Appels de Présence / Épreuves (Contrôle)</h5>
    <p>Le module d'appel n'est <strong>pas seulement utilisé pour les devoirs ou compositions</strong>. Il sert également au <strong>contrôle de présence quotidien des élèves</strong> (absence, retard, etc.). <br>
    Les pénalités configurées déduisent automatiquement des points sur la note de conduite de l'élève (qui démarre à 18/20). Des notifications sont envoyées aux parents en cas d'alerte configurée.</p>
    <h5 class="fw-bold mt-4">Évaluations et Emploi du Temps</h5>
    <p>Gérez la grille d'emploi du temps, et saisissez les notes des élèves.</p>
    <h5 class="fw-bold mt-4">Bulletins</h5>
    <p>Générables par mois (composition mensuelle) ou trimestre. L'impression peut être individuelle ou par lot (une classe complète). Les bulletins respectent le format institutionnel du Mali.</p>

    <h4 class="fw-bold text-uppercase mt-5 mb-3 border-bottom pb-2">Étape 5 — Enseignants et Salaires</h4>
    <h5 class="fw-bold mt-4">Gestion du Personnel</h5>
    <p>Création des profils avec type de contrat (Fonctionnaire, CDI, CDD, Vacataire). Impression de la fiche enseignant et de la <strong>carte professionnelle</strong>.</p>
    <h5 class="fw-bold mt-4">Présences et Émargements</h5>
    <p>Suivi via le <strong>Cahier de présence</strong> (souvent pour Fondamental I) ou <strong>Émargements</strong> (Fondamental II, Secondaire). Les heures validées alimentent directement le calcul des salaires.</p>
    <h5 class="fw-bold mt-4">Salaires et Paiements</h5>
    <ul>
        <li>Calcul dynamique selon le type de contrat et les mois de l'année scolaire (Mode 12/12 ou 9/12).</li>
        <li>Paiement individuel (avec décaissement automatique de la caisse) ou régularisation groupée des arriérés.</li>
        <li>Génération de l'<strong>État des salaires</strong> (Mandat de paiement) et du <strong>Bulletin de salaire individuel</strong> (PDF A5).</li>
    </ul>

    <h4 class="fw-bold text-uppercase mt-5 mb-3 border-bottom pb-2">Étape 6 — Finances</h4>
    <p>Module centralisant les flux financiers de l'école.</p>
    <ul>
        <li><strong>Planification :</strong> Création rapide d'échéanciers appliqués à plusieurs classes d'un coup.</li>
        <li><strong>Paiements et Frais scolaires :</strong> Encaissement direct, paiements groupés. Le système génère automatiquement des <strong>reçus PDF normaux</strong> ou des <strong>reçus thermiques</strong>.</li>
        <li><strong>Caisse :</strong> Chaque paiement nécessite une caisse active. Les décaissements ou encaissements libres y sont tracés.</li>
    </ul>

    <h4 class="fw-bold text-uppercase mt-5 mb-3 border-bottom pb-2">Étape 7 — Espace Parents</h4>
    <p>Créez les comptes parents et rattachez-y leurs enfants. Un parent connecté voit uniquement les informations de ses enfants (montant à payer, retards, dossier élève, téléchargement des reçus, etc.).</p>

    <h4 class="fw-bold text-uppercase mt-5 mb-3 border-bottom pb-2">Étape 8 — Abonnements</h4>
    <p>Permet à l'école de gérer sa licence KalanNet. Le paiement manuel se fait puis est validé par le Super Administrateur. Une alerte apparaît une semaine avant l'expiration.</p>
